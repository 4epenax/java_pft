package ru.stqa.pft.sandbox.learn.loops;

public class ForExample {

    public static void main(String[] args) {
        // for (initialization(инициализация); termination(условие выполнения); increment(изменение - наращивание или истощение))
        for (int i = 1; i < 6; i++) {
            System.out.println("Строка №" + i);
        }
    }
}
